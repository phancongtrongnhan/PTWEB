                            <label class="form-label">'. ($name = $_FILES["fileToUpload"]["name"]) .'</label>
                        </div>
                        <div class="mb-3">
                            <label class="form-label">'. ($name = $_FILES["fileToUpload"]["type"]) .'</label>
                        </div>
                        <div class="mb-3">
                        <label class="form-label">File size '. ($name = $_FILES["fileToUpload"]["size"]) .'</label>
                    </div>
                        <button type="submit" class="btn btn-primary" name="submit">Go Back</button>
                    </form>
                </div>
            </div>';
        } else {
            echo "Sorry, there was an error uploading your file.";
        }
